private void ultimaFechaCobro (){
       MetodosGenerales generales = new MetodosGenerales();
       String fecha = generales.ultimaFechaCobro("Cobros.txt");
       if(fecha.equals("no")){
     JOptionPane.showMessageDialog(null,"No hay cobros con el status en false que se puedan reversar");
     fechaReversar.setDate(null);
       }else{
           Date fechaCobro = new Date(fecha);
           fechaReversar.setDate(fechaCobro);
       }
   }
