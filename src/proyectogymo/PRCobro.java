    private void jLabel20MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel20MouseClicked
        if(fechaReversar.getCalendar()!=null){
            MetodosGenerales generales = new MetodosGenerales();
            SimpleDateFormat fomato_fecha = new SimpleDateFormat("MM/dd/YYYY");
            String fechaReserva = fomato_fecha.format(fechaReversar.getCalendar().getTime());
            System.out.println(fechaReserva);
            ArrayList<String> vector = generales.LlenarTablaCondicion("Cobros.txt",1,fechaReserva);
            for (int i = 0; i < vector.size(); i++) {
                if(vector.get(i).split("~")[5].equals("false")){
                    String Cliente = generales.validarID("Clientes.txt", vector.get(i).split("~")[2]);
                    String valorBalance = String.valueOf(Integer.parseInt(Cliente.split("~")[12]) - Integer.parseInt(vector.get(i).split("~")[3]));
                    String datosClientes = Cliente.split("~")[0]+"~"+Cliente.split("~")[1]+"~"+Cliente.split("~")[2]+"~"+Cliente.split("~")[3]+"~"+Cliente.split("~")[4]+"~"+Cliente.split("~")[5]+"~"+Cliente.split("~")[6]+"~"+Cliente.split("~")[7]+"~"+Cliente.split("~")[8]+"~"+Cliente.split("~")[9]+"~"+Cliente.split("~")[10]+"~"+Cliente.split("~")[11]+"~"+valorBalance+"~"+Cliente.split("~")[13];
                    generales.modificar("Clientes.txt", Cliente.split("~")[0], datosClientes);
                    generales.eliminar("Cobros.txt",vector.get(i).split("~")[0]);

                }
            }

            ultimaFechaCobro();
        }
    }//GEN-LAST:event_jLabel20MouseClicked

