    private void idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyReleased
        MetodosGenerales usuario= new MetodosGenerales();
        String consulta=usuario.validarID("Clientes.txt", id.getText());
        if(consulta.equals("no")){
            nombre.setText("");
            paterno.setText("");
            materno.setText("");
            direccion.setText("");
            nacimiento.setDate(null);
            telefono.setText("");
            celular.setText("");
            Date fechaHoy = new Date();
            ingreso.setDate(fechaHoy);
            estado.setSelected(false);
            tipo.setSelectedIndex(0);
            correo.setText("");
            cuota.setText("");
            balance.setText("");
            mensaje.setText("CREANDO");
        }else{

            nombre.setText(consulta.split("~")[1]);
            paterno.setText(consulta.split("~")[2]);
            materno.setText(consulta.split("~")[3]);
            direccion.setText(consulta.split("~")[4]);
            Date FechaNacimiento = new Date(consulta.split("~")[5]);
            nacimiento.setDate(FechaNacimiento);
            telefono.setText(consulta.split("~")[6]);
            celular.setText(consulta.split("~")[7]);
            Date FechaIngreso = new Date(consulta.split("~")[8]);
            ingreso.setDate(FechaIngreso);
            estado.setSelected(Boolean.valueOf(consulta.split("~")[9]));
            tipo.setSelectedItem(consulta.split("~")[10]);
            correo.setText(consulta.split("~")[11]);
            balance.setText(consulta.split("~")[12]);
            cuota.setText(consulta.split("~")[13]);
            mensaje.setText("Modificando");
        }

    }//GEN-LAST:event_idKeyReleased

