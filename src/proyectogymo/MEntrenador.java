    private void idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyReleased
        MetodosGenerales usuario= new MetodosGenerales();
        String consultas_usu=usuario.validarID("Entrenadores.txt", id.getText());
        if(consultas_usu.equals("no")){
            nombre.setText("");
            apellido.setText("");
            telefono.setText("");
            correo.setText("");
            mensaje.setText("CREANDO");
        }else{
            nombre.setText(consultas_usu.split("~")[1]);
            apellido.setText(consultas_usu.split("~")[2]);
            telefono.setText(consultas_usu.split("~")[3]);
            correo.setText(consultas_usu.split("~")[4]);

            mensaje.setText("Modificando");
        }
    }//GEN-LAST:event_idKeyReleased

    private void jLabel13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel13MouseClicked
        if(id.getText().equals("")==false && nombre.getText().equals("")==false && apellido.getText().equals("")==false && telefono.getText().equals("")==false){
            MetodosGenerales general = new MetodosGenerales ();
            String validar=general.validarID("Entrenadores.txt",id.getText());
            if(validar.equals("no")){
                String validar_email="Sin email";
                if(correo.getText().equals("")){
                    validar_email="Sin email";
                }else{
                    validar_email=correo.getText();
                }
                general.registrar("Entrenadores.txt",id.getText()+"~"+nombre.getText()+"~"+apellido.getText()+"~"+telefono.getText()+"~"+validar_email);
                id.setText("");
                nombre.setText("");
                apellido.setText("");
                telefono.setText("");
                correo.setText("");
                mensaje.setText("CREANDO");

            }else{
                general.modificar("Entrenadores.txt",id.getText(),id.getText()+"~"+nombre.getText()+"~"+apellido.getText()+"~"+telefono.getText()+"~"+correo.getText());

            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios, por favor llenarlo","Errro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel13MouseClicked

    private void jLabel14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel14MouseClicked
        if(id.getText().equals("")==false){
            MetodosGenerales borrar =new MetodosGenerales();
            borrar.eliminar("Entrenadores.txt", id.getText());
            id.setText("");
            nombre.setText("");
            apellido.setText("");
            telefono.setText("");
            correo.setText("");
            mensaje.setText("CREANDO");

        }
    }//GEN-LAST:event_jLabel14MouseClicked

