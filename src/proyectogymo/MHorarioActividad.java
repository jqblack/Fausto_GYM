    private void jLabel13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel13MouseClicked
        if(id.getText().equals("")==false && dia.getText().equals("")==false && hora.getText().equals("")==false && actividad.getText().equals("")==false){
            MetodosGenerales general = new MetodosGenerales ();
            String validar=general.validarID("HorarioActividades.txt",id.getText());
            if(validar.equals("no")){
                general.registrar("HorarioActividades.txt",id.getText()+"~"+dia.getText()+"~"+hora.getText()+"~"+actividad.getText());
                id.setText("");
                dia.setText("");
                hora.setText("");
                actividad.setText("");
            }else{
                general.modificar("HorarioActividades.txt",id.getText(),id.getText()+"~"+dia.getText()+"~"+hora.getText()+"~"+actividad.getText());

            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios, por favor llenarlo","Errro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel13MouseClicked

