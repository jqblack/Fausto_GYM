    private void jLabel13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel13MouseClicked
        if(id.getText().equals("")==false && fecha.getCalendar()!=null && fechaBaja.getCalendar()!=null && estado.getText().equals("")==false && cliente.getText().equals("")==false && hora.getText().equals("")==false && actividad.getText().equals("")==false ){
            MetodosGenerales general = new MetodosGenerales ();
            String validar=general.validarID("ReservasActividades.txt",id.getText());
            SimpleDateFormat fomato_fecha = new SimpleDateFormat("MM/dd/YYYY");
            String fechaReserva = fomato_fecha.format(fecha.getCalendar().getTime());
            String fechaReservaBaja = fomato_fecha.format(fechaBaja.getCalendar().getTime());

            if(validar.equals("no")){
                general.registrar("ReservasActividades.txt",id.getText()+"~"+fechaReserva+"~"+fechaReservaBaja+"~"+estado.getText()+"~"+cliente.getText()+"~"+actividad.getText()+"~"+hora.getText());
                id.setText("");
                fecha.setDate(null);
                fechaBaja.setDate(null);
                estado.setText("");
                cliente.setText("");
                hora.setText("");
                actividad.setText("");
                mensaje.setText("CREANDO");
            }else{
                general.modificar("ReservasActividades.txt",id.getText(),id.getText()+"~"+fechaReserva+"~"+fechaReservaBaja+"~"+estado.getText()+"~"+cliente.getText()+"~"+actividad.getText()+"~"+hora.getText());
            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios, por favor llenarlo","Errro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel13MouseClicked

    private void jLabel14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel14MouseClicked
        if(id.getText().equals("")==false){
            MetodosGenerales borrar =new MetodosGenerales();
            String consulta=borrar.validarID("ReservasActividades.txt", id.getText());
            if(consulta.equals("no")){
                JOptionPane.showMessageDialog(null,"El id no existe","Error",JOptionPane.ERROR_MESSAGE);
            }else{
                borrar.eliminar("ReservasActividades.txt", id.getText());
                id.setText("");
                fecha.setDate(null);
                fechaBaja.setDate(null);
                estado.setText("");
                cliente.setText("");
                hora.setText("");
                actividad.setText("");
                mensaje.setText("CREANDO");
            }

        }
    }//GEN-LAST:event_jLabel14MouseClicked

