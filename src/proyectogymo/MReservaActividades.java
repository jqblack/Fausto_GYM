    private void idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyReleased
        MetodosGenerales usuario= new MetodosGenerales();
        String consulta=usuario.validarID("ReservasActividades.txt", id.getText());
        if(consulta.equals("no")){
            fecha.setDate(null);
            fechaBaja.setDate(null);
            estado.setText("");
            cliente.setText("");
            hora.setText("");
            actividad.setText("");
            mensaje.setText("CREANDO");
        }else{
            Date fecha_Reserva = new Date(consulta.split("~")[1]);
            fecha.setDate(fecha_Reserva);
            Date fecha_Baja = new Date(consulta.split("~")[2]);
            fechaBaja.setDate(fecha_Baja);
            estado.setText(consulta.split("~")[3]);
            cliente.setText(consulta.split("~")[4]);
            actividad.setText(consulta.split("~")[5]);
            hora.setText(consulta.split("~")[6]);
            mensaje.setText("Modificando");
        }
    }//GEN-LAST:event_idKeyReleased

