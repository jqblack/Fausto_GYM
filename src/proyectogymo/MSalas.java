    private void jLabel13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel13MouseClicked
        if(id.getText().equals("")==false && nombre.getText().equals("")==false && descripcion.getText().equals("")==false && localizacion.getText().equals("")==false){
            MetodosGenerales general = new MetodosGenerales ();
            String validar=general.validarID("Salas.txt",id.getText());
            if(validar.equals("no")){

                general.registrar("Salas.txt",id.getText()+"~"+nombre.getText()+"~"+descripcion.getText()+"~"+localizacion.getText());
                id.setText("");
                nombre.setText("");
                descripcion.setText("");
                localizacion.setText("");
                mensaje.setText("CREANDO");

            }else{
                general.modificar("Salas.txt",id.getText(),id.getText()+"~"+nombre.getText()+"~"+descripcion.getText()+"~"+localizacion.getText());

            }
        }else{
            JOptionPane.showMessageDialog(null,"Todos los campos son obligatorios, por favor llenarlo","Errro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel13MouseClicked

    private void jLabel14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel14MouseClicked
        if(id.getText().equals("")==false){
            MetodosGenerales borrar =new MetodosGenerales();
            borrar.eliminar("Salas.txt", id.getText());
            id.setText("");
            nombre.setText("");
            descripcion.setText("");
            localizacion.setText("");
            mensaje.setText("CREANDO");

        }
    }//GEN-LAST:event_jLabel14MouseClicked

